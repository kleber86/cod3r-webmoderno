// while
let contador = 1
while (contador <= 10) {
    console.log(`Contador = ${contador}`)
    contador++
}

// for #1
for (let i = 1; i <= 10; i++) {
    console.log(`Contador = ${i}`)
}

// for #2
const notas = [6.7, 7.4, 9.8, 7.7]
for (let i = 0; i < notas.length; i++) {
    console.log(`nota = ${notas[i]}`)
}